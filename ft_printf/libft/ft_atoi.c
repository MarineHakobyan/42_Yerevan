int	ft_atoi(const char *str)
{
	int	num;
	int	i;
	int	min;

	min = 1;
	i = 0;
	num = 0;
	while ((str[i] == 32) || (str[i] >= 9 && str[i] <= 13))
			++i;
	if ((str[i] == '-' || str[i] == '+')
		&& (str[i + 1] >= 48 && str[i + 1] <= 57))
	{
		if (str[i] == '-')
		min *= -1;
		++i;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10;
		num = num + str[i] - 48;
		++i;
	}
	return (num * min);
}
